people = 20
cats = 30
dogs = 15

=begin
if people < cats
  puts "Too many cats!"
end

if people > cats
  puts "Not too many cats..."
end

if people < dogs
  puts "The world is drooled on!"
end

if people > dogs
  puts "Whew! The world is dry..."
end
=end

dogs += 6

if people >= dogs
  puts "People are greater than or equal to Dogs."
end

if people <= dogs
  puts "People are less than or equal to Dogs."
end

if people == dogs
  puts "People ARE dogs..."
end
